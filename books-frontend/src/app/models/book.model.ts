/**
 * اسم الملف: book.model.ts
 * الوصف: واجهة برمجية (Interface) تحدد هيكل كائن "الكتاب" داخل نظام الأنجولار.
 * تضمن هذه الواجهة توافق أنواع البيانات القادمة من API مع المكونات (Components).
 */

export interface Book {
  /** المعرف الفريد للكتاب (اختياري لأنه يتولد في قاعدة البيانات) */
  _id?: string;

  /** عنوان الكتاب أو اسم المؤلف */
  name: string;

  /** نوع الكتاب: مقيد بخيارين فقط (عام أو مخصص) لضمان دقة البيانات */
  type: 'عام' | 'مخصص';

  /** التصنيف اللغوي أو الموضوعي للكتاب */
  category: 'ترفيهي' | 'ثقافي' | 'ديني' | 'تقني' | 'صحي' | 'رياضي';

  /** شرح مفصل لمحتويات الكتاب */
  description: string;

  /** اسم ملف الصورة أو المسار الخاص بالغلاف */
  image: string;

  /** تاريخ إنشاء السجل (اختياري - يأتي من خاصية timestamps في Mongoose) */
  createdAt?: Date;

  /** تاريخ آخر تحديث للسجل (اختياري) */
  updatedAt?: Date;
}